#include<stdio.h>

int main()
{
    int a = 10,b =1;
    a += b; //a = a + b
    a -= b; //a = a - b
    a *= b; //a = a * b
    a /= b; //a = a / b
    a %= b; //a = a % b
    a &= b; //a = a & b
    a |= b; //a = a | b
    a ^= b; //a = a ^ b
    a <<= b; //a = a << b
    a >>= b; //a = a >> b

    return 0;
}